/* Find the skills required by these jobs to understand which skills
are most valued in the industry*/

WITH top_10_data_analyst_jobs AS (
            SELECT
                    jpf.job_id,
                    jpf.job_title,
                    company_dim.name AS company_name,
                    jpf.salary_year_avg
            FROM    
                    job_postings_fact AS jpf
            LEFT JOIN
                    company_dim ON
                    company_dim.company_id = jpf.company_id     
            WHERE
                    job_location = 'Anywhere' AND
                    salary_year_avg IS NOT NULL AND
                    job_title_short = 'Data Analyst'
            ORDER BY
                    salary_year_avg DESC
            LIMIT 10
)

SELECT 
        top_10.*,
        skills.skills AS skill_name
FROM
         top_10_data_analyst_jobs AS top_10
INNER JOIN 
        skills_job_dim ON
        skills_job_dim.job_id = top_10.job_id 
INNER JOIN 
        skills_dim AS skills ON
        skills.skill_id = skills_job_dim.skill_id
ORDER BY
        top_10.salary_year_avg DESC

